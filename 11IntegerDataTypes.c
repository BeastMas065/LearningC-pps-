#include<stdio.h>
int main()
{
    int a = -100;
    unsigned int b = 1000;
    short int c = -3000;
    unsigned short int d = 6000;
    long int e = 1000;
    unsigned long int f = 4000000;
    printf("int a = %d\n", a);
    printf("unsigned int b = %u\n", b);
    printf("short int c = %d\n", c);
    printf("unsigned short int d = %u\n", d);
    printf("long int e = %ld\n", e);
    printf("unsigned long int f = %lu\n", f);
    return 0;
}